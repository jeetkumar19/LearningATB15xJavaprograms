package ex_04_literals;

public class Lab0018_char_literals {
    static void main() {
        char c1='r';
        char c2='e';
        System.out.println(c2);
        // escape sequence
        char _new_line ='\n';
        char tab_line ='\t';
        char blank_space='\b';
        char carriage_return='\r';

        System.out.println("jeet kumar");
        System.out.println("jeet\nkmar");
        System.out.println("jeet\tkumar");
        System.out.println("jeet\bkumar");
        System.out.println("jeet\rkumar");




    }
}
