package ex_04_literals;

public class Lab0026_adding_mini_challenge_literals {

        public static void main(String[] args) {

            int decimal = 10;
            int binary = 0b1010;
            int octal = 012;
            int hex = 0xA;

            float floatLiteral = 10.5f;
            char charLiteral = 'A';
            String stringLiteral = "Java";
            boolean boolLiteral = true;

            System.out.println("Decimal: " + decimal);
            System.out.println("Binary: " + binary);
            System.out.println("Octal: " + octal);
            System.out.println("Hexadecimal: " + hex);

            System.out.println("Float: " + floatLiteral);
            System.out.println("Char: " + charLiteral);
            System.out.println("String: " + stringLiteral);
            System.out.println("Boolean: " + boolLiteral);
        }
    }


