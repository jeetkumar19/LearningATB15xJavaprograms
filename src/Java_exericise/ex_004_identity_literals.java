public class ex_004_identity_literals {
    static void main(String[] args) {
        int a = 101;
        float f = 10.5f;
        long l = 100000L;
        boolean flag = true;
        System.out.println(a);
        System.out.println(f);
        System.out.println(l);
        System.out.println(flag);


    }
}
